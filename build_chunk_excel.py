    "\n",
    "                # Info temporali\n",
    "                \"datetime_str\": datetime_str,\n",
    "                \"year\": year_val,\n",
    "                \"month\": month,\n",
    "                \"day\": day,\n",
    "                \"hour\": hour,\n",
    "                \"sec\": sec,\n",
