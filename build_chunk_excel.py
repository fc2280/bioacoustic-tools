    "                # Annotazioni chunk\n",
    "                \"chunk_presence\": \"\",\n",
    "                \"chunk_sound_type\": \"\",\n",
    "                \"chunk_confidence\": \"\",\n",
    "                \"chunk_notes\": \"\"\n",
