    "            # ==============================\n",
    "            # Estrazione chunk id\n",
    "            # ==============================\n",
    "\n",
    "            # Cerca \"chunk_XX\" nel nome file\n",
    "            match = re.search(r\"chunk_(\\d+)\", chunk.name)\n",
    "\n",
    "            if match:\n",
    "                chunk_id = int(match.group(1))\n",
    "            else:\n",
    "                chunk_id = None\n",
    "\n",
    "\n",
    "            # ==============================\n",
    "            # Parsing datetime\n",
    "            # ==============================\n",
    "\n",
    "            datetime_str = None\n",
    "            year_val = None\n",
    "            month = None\n",
    "            day = None\n",
    "            hour = None\n",
    "            sec = None\n",
    "\n",
    "            try:\n",
    "\n",
    "                # Nome recording esempio:\n",
    "                # REC_20140429_230000\n",
    "                parts = rec.name.split(\"_\")\n",
    "\n",
    "                # Estrazione anno mese giorno\n",
    "                year_val = parts[1][:4]\n",
    "                month = parts[1][4:6]\n",
    "                day = parts[1][6:8]\n",
