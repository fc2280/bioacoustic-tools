    "print(\"==============================\\n\")\n",
    "\n",
    "\n",
    "# ==============================\n",
    "# Input utente\n",
    "# ==============================\n",
    "\n",
    "# Chiede la directory base degli spettrogrammi\n",
    "# esempio: /Volumes/FC2280/fc2280\n",
    "base_dir = input(\"Spectrogram base directory: \").strip('\"')\n",
    "\n",
    "# Chiede l'anno da processare\n",
    "# esempio: 2023\n",
    "year = input(\"Year (e.g. 2023): \")\n",
    "\n",
    "\n",
    "# ==============================\n",
    "# Costruzione path base\n",
    "# ==============================\n",
    "\n",
    "# Costruisce il path:\n",
    "# /Volumes/FC2280/fc2280/2023\n",
    "base_path = Path(base_dir) / year\n",
    "\n",
    "\n",
    "# ==============================\n",
    "# Livelli spettrogrammi\n",
    "# ==============================\n",
    "\n",
    "# Le cartelle create da generate_spectrograms.py\n",
    "levels = [\"LOW\", \"MEDIUM\", \"HIGH\"]\n",
    "\n",
    "\n",
    "# ==============================\n",
    "# Lista righe excel\n",
    "# ==============================\n",
    "\n",
    "# Lista vuota dove verranno salvate le righe Excel\n",
    "rows = []\n",
    "\n",
    "\n",
