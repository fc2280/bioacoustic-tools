    "import pandas as pd\n",
    "\n",
    "# pathlib serve per lavorare con file e cartelle\n",
    "from pathlib import Path\n",
    "\n",
    "# re serve per leggere il numero dei chunk dal nome file\n",
    "import re\n",
    "\n",
    "\n",
    "# ==============================\n",
    "# Header\n",
